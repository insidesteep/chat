const ButtonGroup = ({ titles = [], type = "default", name, icons }) => {
  const iconTypeClass = type === "icon" ? "radio-btn__group--icon" : "";
  const radioName = uuidv4();

  return (
    <div className={`radio-btn__group ${iconTypeClass}`}>
      {titles.map((t, idx) => (
        <div key={idx} className="radio-btn">
          <input
            className="radio-btn__input"
            type="radio"
            name={name ? name : radioName}
            id={t}
            defaultChecked
          />
          <label className="radio-btn__wrapper" htmlFor={t}>
            {t}
          </label>
        </div>
      ))}
      {titles.length === 0 &&
        routes.map((route) => (
          <div key={route.path} className="radio-btn">
            <NavLink to={route.path}>
              <span className="radio-btn__wrapper">{route.icon}</span>
            </NavLink>
          </div>
        ))}
    </div>
  );
};

export default ButtonGroup;
